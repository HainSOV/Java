package java_lectures;
public class array {
 public static void main(String[] args) {
    int[ ] arr = new int[10]; // пустой массив с 10 элементами 
    arr[3] = 14; // назвначить 3 элемент массива равный 14
    System.out.println(arr[3]); // вывести на экран 3 элемент массива
    int[ ] arr1 = new int[] {1,2,3,4}; //массив с данными значениями 
    System.out.println(arr1[3]);
 }   
}
