package java_lectures;
public class peremenie {
    public static void main(String[] args) {
        int a = 123;
        int b = 321;
        System.out.println(a ++); // преоретет меньше чем у вывода
        System.out.println(a);
        System.out.println(++ b); // преоретет больше чем у вывода
        System.out.println(b);

    }
}
